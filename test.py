TRAIN_PATH = 'data/ns_V1e-3_N5000_T50.mat'
TEST_PATH = 'data/ns_V1e-3_N5000_T50.mat'

ntrain = 4500
ntest = 500

modes = 12
width = 20

batch_size = 20
learning_rate = 0.001
epochs = 500
iterations = epochs*(ntrain//batch_size)

path = 'ns_fourier_2d_time_N'+str(ntrain)+'_ep' + str(epochs) + '_m' + str(modes) + '_w' + str(width)
path_model = 'model/'+path
